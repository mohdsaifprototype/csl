                        NSQF is expected to integrate voc cational education, skill training, general education, technical education and the job market.

                        "By 2020 an educational qualification will not be enough. You would need a NSQF to apply for any vacancy in the government," said Minister of State for Skill Development and Entrepreneurship, Rajiv Pratap Rudy.

                        NSQF was notified in December 2013 and it provides a five year implantation period. It states that by recruitment rules of the Government of India and PSUs of the central government will be amended to define eligibility criteria for all positions in terms of NSQF levels. The state governments and their PSUs will also be encouraged to amend their recruitment rules accordingly.

