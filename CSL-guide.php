			<h1>1. CSL Corona Awareness </h1>
			<div class="outer">
				<hr>

				<p>
					Coronaviruses refer to the family of viruses that are known to cause respiratory infections in both animals and humans. There are seven coronaviruses that are currently known to affect humans, out of which four are commonly found across the globe and cause mild cold like symptoms only.The remaining three are known to cause severe diseases such as Middle East Respiratory Syndrome, or MERS caused by MERS-CoV, Severe Acute Respiratory Syndrome caused by SARS-CoV and finally Coronavirus Disease 2019 caused by SARS-CoV-2.</p>
				<p>COVID-19 is a novel coronavirus that has never been previously identified in humans. It is zoonotic in nature, which means it can be transmitted from animal to human, and human to human interactions. It was first reported from Wuhan City on 31 December 2019 in China. The most common symptoms of COVID-19 are fever, tiredness, and dry cough. Some patients may have aches and pains, nasal congestion, runny nose, sore throat or diarrhea.</p>
				<p>Around 80% of the infected show only mild symptoms and recover with standard medical attention. Older people and those with an underlying or previous medical condition develop serious symptoms and without medical intervention can prove fatal. Around 14% of the infected become seriously ill and 5% critical as per the studies.
					The virus has affected more than 100,000 globally and has caused more than 3000 deaths. WHO has given the status of Very High Risk at the Global Level.
				</p>
				<hr>
				<h1>2. Precautions and Preventive Measures</h1>
				<hr>
				<p>
					Even though COVID-19 is a highly contagious disease, taking adequate precautions can help prevent you from being infected.
					The virus spread through small droplets from the nose or mouth of the affected person, when the person sneezes or coughs. These droplets can travel more than 1 meter from the source and might land on objects and surfaces. Other people can catch the virus by touching the surface and then touching their eyes, mouth or nose. The virus can also be caught by inhaling these droplets if the distance between the affected person is less than one meter.
				</p>
				<hr>
				<h1>3. I am Infected</h1>
				<hr>
				<p>
					If you are sick with COVID-19 or suspect you are infected with the virus that causes COVID-19, follow the steps below to help prevent the disease from spreading to people in your home and community.
					<hr>
				<h4>Stay home except to get medical care</h4>
				<ul>
					<li><b>Stay home:</b> People who are mildly ill with COVID-19 are able to isolate at home during their illness. You should restrict activities outside your home, except for getting medical care.</li>
					<li><b>Avoid public areas:</b> Do not go to work, school, or public areas</li>
					<li><b>Avoid public transportation:</b> Avoid using public transportation, ride-sharing, or taxis.</li>
				</ul>
				<hr>
				<h4>Separate yourself from other people and animals in your home</h4>
				<ul>
					<li><b>Stay away from others:</b> As much as possible, you should stay in a specific room and away from other people in your home. Also, you should use a separate bathroom, if available.</li>
					<li><b>Limit contact with pets & animals:</b> You should restrict contact with pets and other animals while you are sick with COVID-19, just like you would around other people. Although there have not been reports of pets or other animals becoming sick with COVID-19, it is still recommended that people sick with COVID-19 limit contact with animals until more information is known about the virus.</li>
					<li>When possible, have another member of your household care for your animals while you are sick. If you are sick with COVID-19, avoid contact with your pet, including petting, snuggling, being kissed or licked, and sharing food. If you must care for your pet or be around animals while you are sick, wash your hands before and after you interact with pets and wear a facemask.</li>
				</ul>
				<hr>
				<h4>Call ahead before visiting your doctor</h4>
				<ul>
					<li>If you have a medical appointment, call the healthcare provider and tell them that you have or may have COVID-19. This will help the healthcare provider’s office take steps to keep other people from getting infected or exposed.</li>
				</ul>

				<hr>
				<h4>Wear a facemask if you are sick</h4>
				<ul>
					<li><b>If you are sick:</b> You should wear a facemask when you are around other people (e.g., sharing a room or vehicle) or pets and before you enter a healthcare provider’s office.</li>
					<li><b>If you are caring for others:</b> If the person who is sick is not able to wear a facemask (for example, because it causes trouble breathing), then people who live with the person who is sick should not stay in the same room with them, or they should wear a facemask if they enter a room with the person who is sick.</li>
				</ul>

				<hr>
				<h4>Cover your coughs and sneezes</h4>
				<ul>
					<li><b>Cover:</b> Cover your mouth and nose with a tissue when you cough or sneeze.</li>
					<li><b>Dispose:</b> Throw used tissues in a lined trash can.</li>
					<li><b>Wash hands:</b> Immediately wash your hands with soap and water for at least 20 seconds or, if soap and water are not available, clean your hands with an alcohol-based hand sanitizer that contains at least 60% alcohol.</li>
				</ul>

				<hr>
				<h4>Clean your hands often</h4>
				<ul>
					<li><b>Wash hands:</b> Wash your hands often with soap and water for at least 20 seconds, especially after blowing your nose, coughing, or sneezing; going to the bathroom; and before eating or preparing food.</li>
					<li><b>Hand sanitizer:</b> If soap and water are not readily available, use an alcohol-based hand sanitizer with at least 60% alcohol, covering all surfaces of your hands and rubbing them together until they feel dry.</li>
					<li><b>Soap and water:</b> Soap and water are the best option if hands are visibly dirty.</li>
					<li><b>Avoid touching:</b> Avoid touching your eyes, nose, and mouth with unwashed hands.</li>
				</ul>

				<hr>
				<h4>Avoid sharing personal household items</h4>
				<ul>
					<li><b>Do not share:</b> You should not share dishes, drinking glasses, cups, eating utensils, towels, or bedding with other people or pets in your home.</li>
					<li><b>Wash thoroughly after use:</b> After using these items, they should be washed thoroughly with soap and water.</li>

				</ul>

				<hr>
				<h4>Clean all “high-touch” surfaces everyday</h4>
				<ul>
					<li><b>Clean and disinfect:</b> Practice routine cleaning of high touch surfaces. **High touch surfaces include counters, tabletops, doorknobs, bathroom fixtures, toilets, phones, keyboards, tablets, and bedside tables.</li>
					<li><b>Disinfect areas with bodily fluids:</b> Also, clean any surfaces that may have blood, stool, or body fluids on them.</li>
					<li><b>Household cleaners:</b> Use a household cleaning spray or wipe, according to the label instructions. Labels contain instructions for safe and effective use of the cleaning product including precautions you should take when applying the product, such as wearing gloves and making sure you have good ventilation during use of the product.</li>
				</ul>

				<hr>
				<h4>Monitor your symptoms</h4>
				<ul>
					<li><b>Seek medical attention:</b> Seek prompt medical attention if your illness is worsening (e.g., difficulty breathing).</li>
					<li><b>Call your doctor:</b> Before seeking care, call your healthcare provider and tell them that you have, or are being evaluated for, COVID-19.</li>
					<li><b>Wear a facemask when sick:</b> Put on a facemask before you enter the facility. These steps will help the healthcare provider’s office to keep other people in the office or waiting room from getting infected or exposed.</li>
					<li><b>Alert health department:</b> Ask your healthcare provider to call the local or state health department. Persons who are placed under active monitoring or facilitated self-monitoring should follow instructions provided by their local health department or occupational health professionals, as appropriate.</li>
				</ul>

				<hr>
				<h4>Discontinuing home isolation</h4>

				<ul>
					<li><b>Stay at home until instructed to leave:</b> Patients with confirmed COVID-19 should remain under home isolation precautions until the risk of secondary transmission to others is thought to be low.</li>
					<li><b>Talk to your healthcare provider:</b> The decision to discontinue home isolation precautions should be made on a case-by-case basis, in consultation with healthcare providers and state and local health departments.</li>
				</ul>
				</p>
				<hr>
				<h1>4. Symptoms</h1>
				<hr>
				<p>
					The most common symptoms of COVID-19 are fever, tiredness, and dry cough. They are usually mild and begin gradually. They appear around 2 - 14 days with average symptoms showing up after 5 days of being infected.
					Some symptoms appear less common than others. The major symptoms of COVID-19 based on how common it appears in the reported cases are
				<ul>
					<li>Fever </li>
					<li>Dry Cough </li>
					<li>Fatigue </li>
					<li>Sputum Production </li>
					<li>Shortness of Breath </li>
					<li>Muscle Pain or Joint Pain</li>
					<li>Sore Throat </li>
				</ul>
				</p>
				<hr>
				<h1> 5. Guidelines for Home Quarantine</h1>
				<hr>
				<p>
				<h4>Introduction</h4>
				<ul>
					<li>Persons and household members should be educated about personal hygiene, basic IPC measures and how to care for the member of the family suspected of having COVID-19 disease as safely as possible to prevent the infection from spreading to household contacts.</li>
					<li>The persons and the family should be provided with ongoing support and education, and monitoring should continue for the duration of home care.</li>
				</ul>
				</p>
				<hr>
				<h1>Recommendations</h1>
				<p>

					Persons and families should adhere to the following recommendations:-

				<ul>

					<li>Place the person in a well-ventilated single room (i.e . with open windows and an open door)</li>
					<li>Limit the movement of the person in the house and minimize shared space. Ensure that shared spaces (e.g., kitchen, bathroom) are well ventilated (e.g., keep windows open).</li>
					<li>Household members should stay in a different room or, if that is not possible. maintain a distance of at least 1 mt from the ill person.</li>
					<li>Limit the number of caregivers, ideally, assign one person who is in a good health and has no underlying chronic or immuno compromised conditions. Visitors should not be allowed until the person has completely recovered and has no signs and symptoms.</li>
					<li>Perform hand hygiene after any type of contact with person or their immediate environment. Hand hygiene should also be performed before and after preparing food, before eating, after using the toilets and whenever hands look dirty. If hands are not visibly dirty. an alcohol-based hand rub can be used For visibly dirty hands, use soap and water.</li>
					<li>When washing hands with soap and water, it is preferable to use
						disposable paper towels to dry hands. If these are not available. use clean cloth rowels and replace them when they become wet. Disposable paper towels should be disposed off in closed bins only.</li>
					<li>To contain respiratory secretions, a medical mask should be provided to the person and worn as much as possible. Materials used to cover the mouth and nose should be discarded or cleaned appropriately after use (e.g., wash handkerchiefs using regular soap or detergent and water).</li>
					<li>Caregivers should wear a tightly fitted medical mask that covers their mouth and nose when in the same room as the person. Masks should not be touched or handled during use If the mask gets wet or dirty from secretions, it must be replaced immediately with a new clean, dry mask. Remove the mask using the appropriate technique — that is, do not touch the front, but instead untie it Discard the mask immediately after use and perform hand hygiene.</li>
					<li>Avoid direct contact with body fluids, particularly oral or respiratory
						secretions, and stool. Use disposal gloves and a mask when providing oral or respiratory care and when handling stool, urine. and other waste. Perform hand hygiene before and after removing gloves and mask.</li>
					<li>Do not reuse masks or gloves</li>
					<li>Use dedicated linen and eating utensils for the person; these items should
						be cleaned with soap and water after use and may be re-used instead of being discarded. They should be washed at 60-90 oC with common household detergent, and dry thoroughly. Place contaminated linen into a laundry bag.</li>
					<li>Clean and disinfect daily surfaces that are frequently touched in the room where the person is being cared for, such as bedside tables. bed frames and other bedroom furniture. Regular household soap or detergent should be used first for cleaning, and then, after rinsing, regular household disinfectant containing 0.5% sodium hypochlorite ( i.e., equivalent to 5000 pm or 1 part bleach 5 to 9 parts water) should be applied.</li>
					<li>Clean and disinfect bathroom and toilet surfaces at least once daily. Regular household soap or detergent should be used first for cleaning. and then, after rinsing, regular household disinfectant containing 0.5% to 1% sodium hypochlorite should be applied.</li>
					<li>Avoid other types of exposure to contaminated items from the person's immediate environment (e.g.. do not share toothbrushes, cigarettes. eating utensils, dishes, drinks, towels, washcloths or bed linen).</li>
				</ul>
				</p>

