        <h1>Best Practices to Handle Situational Questions in Interviews</h1><br>
        <div class="content" style="color:white;">
          <h2>CSL Editorial Tue, 04/21/2020 - 11:46</h2>
          <p>Soft-skills such as the ones that are more emotional and affected by the way you generally think can be learned and developed rather easily. But a job will demand more out of you than just emotional intelligence. The first step towards taking a job will be your qualification or any experience. The next and the longest-lasting will be your skill-set. In business terms, this is called Subject Matter Expertise, where ‘subject’ is the core-nature of your job, for example, marketing, finance, administration, etc. The deeper your understanding goes of it, the better you would be able to adjust to conditional circumstances that arise out of nowhere. Situational Interviews have an air of intimidation about them. But to be the ideal candidate recruiters look for, you should walk-in well prepared and self-confident. Your interviewer will be making sure that before giving you the clearance, you display the following qualities.</p>


          <h2>Role Preparedness</h2>
          <p>It is important to understand the profile that you have applied for. Unless you know about your day to day responsibilities, the interviewer will not be impressed. Candidates should begin by secondary searching and compiling the basic details of an organization so that they have ample discussion topics. This can be easily achieved through Wikipedia, the website of the organization, or the simplest of all options, Google. Have a look at the following question asked by one of the interviewers to the candidate:
          </p>
          <p><i><b>Interviewer:</b> "What do you know about the role you're being interviewed for?"</i><br>
            This question is though simple yet it tests whether you, as an interviewee, took the time to understand the job description and more important, go beyond it. Advance preparation would lead you to structure your preliminary doubts and often take control of the conversation.
            Consider the following response for the following client-facing profile:
          </p>
          <p><i><b>Candidate:</b> "I read about it, Sir. It is a Sales and Relationship Management profile which requires me to source new accounts, enhance the relationship with existing customers and focus on profitability"</i><br>
            When it is clear that you took the time for reading the job description, it is implied you take the job opening seriously. Be optimistic, that you have the talent ingredients to convince the interviewer of your superiority over the competition. Provided you can create this impression, the interviewer would begin to see you in a new light. Remember knowledge is power. Before appearing for an interview, always use online resources to find out as much as you can about the role.
          </p>

          <h2>Create Outlook</h2>
          <p>The information you collected in the previous point would help you create an opinion of how businesses work. This is partly because, whether we deliberately notice or not, we see businesses functioning around us all the time. The more data or information you have regarding a company and its competition, or the job vacancy and its associated roles, the better.<br>
            Consider the following question:
          </p>
          <p>
            <i><b>Interviewer:</b> "How would you work towards growing the business?<br>
              What would be your customer acquisition strategy?</i>
          </p>
          <p>Developing a mature level of understanding of the core functions could be accomplished with online resources. Online public forums/blogs list several business books that help build your IQ in terms of revenue maximization. Through the above question, the interviewer wants to calculate how broad your knowledge is about the career you are trying to start or progress.
            Consider the below answer by a marketing professional:
          </p>
          <p><i><b>Candidate:</b> "Marketing is a multi-channel effort. A business’ profits will be directed by its total share of consumers, which can increase by deploying and streamlining tested measures of outreach such as ads, emails, events, etc."</i><br>
            This is just an example. Depending upon your discipline and the job vacancy, the answer could be entirely different. Be sure to learn as much about the role and the company as possible.
          </p>



          <h2>Give & Take Relationship </h2>
          <p>People mostly prefer to work within the same city/state that they have grown up in. After all, they want to go back to their families at the end of a working day. And there is nothing wrong with having such a philosophy. However, there are times, where options for better growth could lay off-shore, in other cities. Let us study another question:
          </p>
          <p><i><b>Interviewer:</b> "Are you open to relocating to another city?"</i><br>
            When a company requires or asks you to relocate to another city, they do so keeping in mind the rising cost of living. For example, living in a metropolitan city like Delhi, Mumbai, or Chennai, would cost more to a professional due to added costs of rent, food, and lifestyle choices. Therefore, it has been seen that the salary relocating candidates incorporates additional allowances and perks. Nevertheless, make sure you clarify this with the interviewer in advance in the following way:
          </p>
          <p><i><b>Candidate:</b> "Yes sir, I might consider that as an option, but will my salary be accommodating the necessary costs of living in that location?"</i><br>
            Remember to clarify the job location well in advance. You should know your final work location and the corresponding salary to not have any complaints later. If the organization does not offer any side-benefits to relocate, you should factor in the future scope of growth within limited means.

          </p>




          <h2>Accepting Business Requirements</h2>
          <p>Depending upon the job profile, professionals might be asked to report during public holidays. Take customer service, for instance, they are expected to work even on national holidays. Having the willingness to work on a scheduled holiday, is very rare, after all, you could have prior commitments. A question in this regard could take the following form:</p>
          <p><i><b>Interviewer:</b> "Would you participate if your branch organizes an event for promotion on a holiday?"</i><br>
            Your parents are experienced, and they would understand if the job requires, you would have to report to the office be it a holiday or not. As mentioned before, the business depends on individual contribution and cooperation. When each one of us treats our responsibilities with the same level of seriousness, results turn a positive page in the balance sheet. Reporting managers especially respect and extend favors to such individuals who they can blindly trust, in times of dire need. Try to cushion the blow for your professional superiors and you’d be able to avail such rewards deemed unsuitable for discussion on this article (wink).<br>
            Reflect on a few possible variations of the following answer:

          </p>
          <p><i><b>Candidate:</b> "Sure sir, I will report at the venue as the business depends on me."</i><br>
            Even though it may be difficult to imagine, sound affirmative (positive) about having done something beyond your regular responsibilities. Such callouts will be occasional and not regular, but a single occasion has the power to make or break your overall impression in the entire office.
          </p>
