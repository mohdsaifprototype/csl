						<li class="inner">Liaise with graphic designers for aesthetic mailer templates.</li>
						<li class="inner">Track and compile results for email campaigns and make correct adjustments/recommendations/implementations for the next one.</li>
						<li class="inner">Innovate strategies for increasing subscriber list from multiple channels.</li>
						<li class="inner">Define standards and email body with optimized content for increased click-through rate. </li>
						<li class="inner">Automate campaigns with set triggers to connect with customers/prospects at peak times.</li>

