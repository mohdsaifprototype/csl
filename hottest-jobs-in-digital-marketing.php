						<li class="inner">Working knowledge of CMS for purposes of integration with automation software.</li>
						<li class="inner">Excellent copywriting and copy-editing skills for creating consumable content for mailers.</li>
						<li class="inner">Enrich customer databases by proposing viable promotions from time to time.</li>
						<li class="inner">Understanding of A/B email split testing, call-to-action copy placement, subject lines, design layouts.</li>
