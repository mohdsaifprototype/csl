				<h1>Top 5 Tips to Stay Productive <br>While you #StayatHome</h1><br>

				<div class="content" style="color:white;">

					<h2>CSL Editorial Wed, 04/15/2020 - 12:46</h2>
					<p>The Coronavirus pandemic has taken the world by storm and brought the global economy to pin-drop silence. With schools and institutional education coming to an indefinite halt, the #Stayathome movement has been blown out of proportion to appear an unending nightmare. But there is a flip side for the student community that could turn the tide on its head and show others the way ahead with non-stop, unhindered learning. Yes, the exams have been delayed, tuition/coaching centers are shut as part of social distancing regulation. But leaders of the education industry have enough learning delivery mechanisms to ensure an uninterrupted supply of lectures. Irrespective of your academic level, be it school or undergraduates, we’ve compiled the following pointers to help you stay put on your journey ahead.</p>
					<ol>
						<li>Creative Planning
							<p>Although there is no official declaration, yet, going by the trends world-over, the lockdown in India may take a good while to come off. Until then, no matter what stage of an academic career you are on, create a calendar and be resolute to stick to it. Of course, the seriousness of it depends on your objective such as preparing for impending entrance examinations (that could happen once the lockdown is lifted) or finishing a project. Whatever it is, pen down the tentative time you need to complete it by breaking down weeks into days and days into time-periods. Just like the ones we followed in school. Suit your style. Decide whether you need prolonged stretches of concentration or small, fragmented periods of 1-2 hours spread evenly throughout the day. Do keep in mind that you have extra time on your hands now that everybody is mandated to #StayatHome. Try not to fall back in the loop that makes you wish for something beyond reach (for now) and focus on how to best make use of this opportunity to #NeverStopLearning.</p>
						</li>
						<li>Make the Most of Your Home Setting
							<p>
								Picking a spot is something that has already been done for students while furnishing the house. Most of us have our separate rooms with attached study tables, bean bags (lucky you) and/or bed-cum sofa set. Don’t overdo this #StayatHome act. Diligently following the time table you created in the previous step within your room is also fine. But it has been seen that people are more focused given a few locational elements such as,
							<ul>
								<li class="inner">With plants around and sitting on a chair in the balcony.</li>
								<li class="inner">Working on the laptop while slouching on the Sofa.</li>
								<li class="inner">Getting a feel for the weather and working on the terrace (if you have one).</li>
							</ul>
							</p>
							<p> People have varying levels of comfort settings and it would prove more productive to find yours. Experiment and see what works best. Most students would simply shut the door and unlock it when it is time to get up. Which brings us to our next point.
							</p>
						</li>
						<li>Manage Distractions Skillfully
							<p>Deep work, as described by Cal Newport in his book of the same title, is your ability to focus without distractions. Staying virtually connected with everyone while practicing social distancing is important. That time is poised to go up now that we all are conditioned to #StayatHome. But this doesn’t mean using your phone for instant-replies, i.e. without there being a need. As per a study, an average person spends up to 3 hours and fifteen minutes on his/her phone daily.</p>
							<p>The constant beeps of a phone are designed to notify, which under some circumstances is important, of an update whereas in others it’s a friend on the other side craving for attention. It would be wise to turn the profile settings of your phone from General to Silent. You’re better off keeping it out of sight until you take a break. Limit the breaks to a duration that allows you to take your mind off your task and upon return lets, you add a fresh perspective. Few of us are night owls when it comes to working. If such is your approach and it works for you then please do so. </p>
						</li>
						<li>Cultivate New Habits
							<p>Life is too short to let go of things when it gives you an opportunity. Humans are creatures of habit. It means that we become what we repeatedly do. Therefore it is important to pay attention to your daily chores and see that your leisure time is both relaxing and refreshes your self. The motive is not to get calculative but smartly introduce new habits and make time and expand your learning. Remember, binge-watching movies were fun up-until you had to forcefully cut slices of free time out of your otherwise busy schedule. This habit-reformation could be both fun and learning such as reading left-over novels that you wanted to finish or a DIY-project proving a hack for an otherwise complicated activity. Youtube is a treasure trove for free tutorials and utilitarian information. How about making robots out of home-scrape and recyclable material. Try and introduce habits that allow you to repurpose knowledge and have fun at the same time. To some, it could be learning to play the guitar, for others it could be learning a new subject which brings us to our final point.</p>
						</li>
						<li>Revitalize Your Skills
							<p>
								We will all get through the Coronavirus pandemic, the history tells us so. With this assurance, students need to understand that in the foreseeable future life, as usual, will hit the tracks running. Perhaps, by the inception of the next term in college or at the beginning of a new session in school, you might look back at this time to check,
							<ul>
								<li class="inner">If you could have done any better?</li>
								<li class="inner">Did you do anything unique at all?</li>
								<li class="inner">How has this period made you better at your discipline, whatever that is, than before?</li>
							</ul>
							<p>Self-guidance is key during the lockdown, and the people who will focus on their merits will emerge as the greatest benefactors. As highlighted above, learning will and should continue. There are plenty of value-adding, short-term, and free online courses available to students. Remote learning has caught the eye of all teachers and students with even kindergarten education being delivered through online sessions. Multi-device compatibility makes attending online classes a dynamic experience. It all begins with you, the student. Your first step could turn into a milestone of a career foundation.<br>
								Do you have the time?<br>
								Yes.<br>
								Are you willing?<br>
								You can think about it and let us know by signing up for the future of learning.
							</p>
							</p>
						</li>
